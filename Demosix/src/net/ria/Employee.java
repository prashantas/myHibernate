package net.ria;

public class Employee {
	
	private int eid;
	private String ename;
	private Long salory;
	
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public Long getSalory() {
		return salory;
	}
	public void setSalory(Long salory) {
		this.salory = salory;
	}
	

}
